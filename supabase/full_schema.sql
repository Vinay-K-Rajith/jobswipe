create index if not exists idx_internships_student on public.internships(student_id);
create index if not exists idx_papers_student on public.research_papers(student_id);
create index if not exists idx_skills_student on public.skills(student_id);
create index if not exists idx_applications_student on public.applications(student_id);
create index if not exists idx_eligibility_student on public.eligibility_results(student_id);
create index if not exists idx_eligibility_company on public.eligibility_results(company_id);
create index if not exists idx_improvement_student on public.improvement_plans(student_id);
create index if not exists idx_bias_recommendations_company on public.bias_recommendations(company_id);
create index if not exists idx_bias_recommendations_created_at on public.bias_recommendations(created_at desc);
create index if not exists idx_fairness_history_company on public.model_fairness_history(company_id);
create index if not exists idx_fairness_history_trained_at on public.model_fairness_history(trained_at desc);
create index if not exists idx_student_interest_student on public.student_interest(student_id);
create index if not exists idx_recruiter_interest_recruiter on public.recruiter_interest(recruiter_id);
create index if not exists idx_matches_student on public.matches(student_id);
create index if not exists idx_matches_recruiter on public.matches(recruiter_id);

